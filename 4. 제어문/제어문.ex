# 4.1 if문
# if문은 다음과 같은 형식으로 작성합니다.4.1 if문
if 조건 do
  # 조건이 참인 경우 수행할 코드
else
  # 조건이 거짓인 경우 수행할 코드
end
# 조건은 참 또는 거짓이 될 수 있는 식(expression)이어야 하며
# 이 식의 값에 따라 if문의 분기 처리가 결정됩니다.
# 예를 들어, 다음과 같은 코드에서는 age 변수의 값이 19 이상인 경우 "성인입니다"를 출력하고
# 그렇지 않은 경우 "미성년자입니다"를 출력합니다.
if age >= 19 do
  IO.puts("성인입니다")
else
  IO.puts("미성년자입니다")
end


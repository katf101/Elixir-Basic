# 4.2 case문
# case문은 패턴 매칭(pattern matching)을 사용하여 여러 가지 조건을 처리할 수 있습니다. case문은 다음과 같은 형식으로 작성합니다.
case 값 do
  패턴1 -> # 패턴1과 일치하는 경우 수행할 코드
  패턴2 -> # 패턴2와 일치하는 경우 수행할 코드
  ...
  패턴n -> # 패턴n과 일치하는 경우 수행할 코드
end
# 값은 패턴 매칭에 사용될 변수나 값입니다.
# 패턴은 변수나 값의 일치 여부를 판단하는 규칙입니다.
# 예를 들어, 다음과 같은 코드에서는 result 변수의 값에 따라서 다른 처리를 수행합니다.
case result do
  :ok -> IO.puts("성공")
  {:error, message} -> IO.puts("오류: #{message}")
  :timeout -> IO.puts("시간 초과")
end
# 위 코드에서는 result 변수의 값이 :ok인 경우 "성공"을 출력하고
# {:error, message}인 경우 "오류: (메시지)"를 출력하고
# :timeout인 경우 "시간 초과"를 출력합니다.
# 이렇게 if문과 case문을 활용하면, 엘릭서에서 다양한 조건 처리를 수행할 수 있습니다.

# 4.3 cond문
# cond(Conditionals)는 엘릭서에서 다양한 조건 처리를 수행할 때 사용하는 구문입니다.
# 조건문의 블록(block)을 연속적으로 나열하여 처리하는 방식으로, if문이나 case문과는 다른 문법을 사용합니다.
# cond는 다음과 같은 형식으로 작성합니다.
cond do
  조건1 -> # 조건1이 참인 경우 수행할 코드
  조건2 -> # 조건2가 참인 경우 수행할 코드
  ...
  true -> # 모든 조건이 거짓인 경우 수행할 코드
end
# cond문은 여러 개의 조건을 처리할 때 주로 사용됩니다.
# 조건은 일반적으로 식(expression)으로 작성되며, 이 식의 값에 따라 처리가 결정됩니다.
# cond문은 else 블록 대신에 true 블록을 사용합니다.
# 이렇게 하면 모든 조건이 거짓인 경우에 해당하는 처리를 수행할 수 있습니다.
# 예를 들어, 다음과 같은 코드에서는 x 변수의 값에 따라서 다른 처리를 수행합니다.
cond do
  x < 0 -> IO.puts("음수입니다")
  x == 0 -> IO.puts("0입니다")
  x > 0 -> IO.puts("양수입니다")
  true -> IO.puts("이상한 값입니다")
end
# 위 코드에서는 x 변수의 값에 따라서 음수, 0, 양수를 판별하고,
# 그 외의 값인 경우 "이상한 값입니다"를 출력합니다.
# 이렇게 cond문을 활용하면, 엘릭서에서 다양한 조건 처리를 수행할 수 있습니다.
